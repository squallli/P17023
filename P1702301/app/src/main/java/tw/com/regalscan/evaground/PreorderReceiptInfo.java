public class PreorderReceiptInfo {
    String preorderReceiptInfo;
    String saleState;
    String page;

    public String getPreorderReceiptInfo() {
        return preorderReceiptInfo;
    }

    public void setPreorderReceiptInfo(String preorderReceiptInfo) {
        this.preorderReceiptInfo = preorderReceiptInfo;
    }

    public String getSaleState() {
        return saleState;
    }

    public void setSaleState(String saleState) {
        this.saleState = saleState;
    }


    public String getPage() {
        return page;
    }

    public void setPage(String page) {
        this.page = page;
    }
}
